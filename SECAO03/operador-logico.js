/*
operadores Logicos

&& - < AND
|| -> OR
! -> NOT
*/

console.log(true && true);
console.log(true && false);
console.log("--------------");
console.log(true || true);
console.log(true || false);
console.log("--------------");
console.log(!true);
console.log(!false);
console.log("--------------");
console.log(!!true); // negando duas vezes (nega uma vez o true, vira false, depois nega o novo false e vira true dnovo)
console.log(!!false);